package ConexaoAtributos;

public class Curso {
	int Cod_Curso;
	String Des_Curso;
	
	public int getCod_Curso() {
		return Cod_Curso;
	}
	public void setCod_Curso(int cod_Curso) {
		Cod_Curso = cod_Curso;
	}
	public String getDes_Curso() {
		return Des_Curso;
	}
	public void setDes_Curso(String des_Curso) {
		Des_Curso = des_Curso;
	}
}
